// Provider ID로 로고 컴포넌트 가져오기
export function getAppLogo(providerId: string, props: LogoProps = {}) {
  const logos: Record<string, React.FC<LogoProps>> = {
    google_drive: GoogleDriveLogo,
    notion: NotionLogo,
    slack: SlackLogo,
    github: GitHubLogo,
    linear: LinearLogo,
    confluence: ConfluenceLogo,
    dropbox: DropboxLogo,
    // 메시징 앱
    'telegram-bot': TelegramLogo,
    telegram: TelegramLogo,
    discord: DiscordLogo,
    whatsapp: WhatsAppLogo,
    'microsoft-teams': TeamsLogo,
    teams: TeamsLogo,
  }

  const LogoComponent = logos[providerId]
  if (!LogoComponent) {
    return null
  }

  return <LogoComponent {...props} />
}

